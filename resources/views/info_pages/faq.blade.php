                    <h3>Wat is Fund a Student?</h3>
                    Fund a Student is een crowdfunding platform dat studenten in staat stelt hun eigen netwerk in te zetten bij het financieren van verschillende studiedoeleinden. Fund a Student is hierin de schakel tussen de student en zijn of haar netwerk.

                    <h3>Hoe werkt het?</h3>
                    Fund a Student werkt eigenlijk heel simpel. Je meldt je aan, start een studie en collegejaar op onze site en kunt het direct delen via Facebook. Zo krijgen al je vrienden en familieleden te zien met welke studie je bezig bent dan dat je wel wat financiële hulp kunt gebruiken bij de betaling van bijvoorbeeld een minor, een studiejaar of een buitenlandexcursie. Je sociale contacten kunnen op een laagdrempelige manier geld doneren.

                    <h3>Mag iedereen een crowdfunding traject starten op Fund a Student?</h3>
                    Nee, daarvoor moet je een student of aankomend student zijn.

                    <h3>Moet ik betalen en/of een abonnement afsluiten om een studiejaar aan te maken op Fund a Student?</h3>
                    Nee, een studiejaar aanmaken op ons platform is geheel gratis!

                    <h2>DONATIES</h2>

                    <h3>Iedereen doneren op Fund a Student?</h3>
