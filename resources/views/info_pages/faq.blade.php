                    <h3>Hoe ontvang ik mijn geld?</h3>
                    Wanneer je een account aanmaakt op Fund a Student vragen we naar je rekeningnummer. Dit klinkt misschien eng, maar wij doen dit zodat we in de toekomst het opgehaalde geld voor je studie rechtstreeks op je rekening kunnen storten. Zo kun je zo snel mogelijk je studie financieren!

                    <h3>Wanneer krijg ik uitbetaald?</h3>
                    Als je een studie aanmaakt op Fund a Student dan geef je daarbij aan hoeveel een studiejaar kost. Op deze manier zorgen wij voor transparantie tegenover de funders. Je kunt je geld te aller tijden opnemen. Wel heeft Fund a Student een uitbetalingsdrempel van €25,00.

                    <h3>Gaat het gefunde bedrag rechtstreeks naar de des betreffende student?</h3>
